
public class HitungUmur {
    public static void main(String[] args) {
        
        int usiaLahir = 2005;
        
        // Menghitung umur berdasarkan tahun saat ini
        int tahunSekarang = 2024;
        int umur = tahunSekarang - usiaLahir;
        
        
        System.out.println("Perkenalkan nama saya Roy damai zalukhu, saya lahir tanggal 14 Januari 2005. sekarang saya berumur : " + umur + " tahun");
    }
}